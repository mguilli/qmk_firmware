static void render_status(void) {
    // QMK Logo and version information
    render_qmk_logo();

    // Host Keyboard Layer Status
    oled_write_P(PSTR("\nLayer: "), false);
    switch (get_highest_layer(layer_state | default_layer_state)) {
        case _NORMAN:
            oled_write_P(PSTR("Norman\n"), false);
            break;
        case _QWERTY:
            oled_write_P(PSTR("Qwerty\n"), false);
            break;
        case _NAV:
            oled_write_P(PSTR("Navigation\n"), false);
            break;
        case _NUMPAD:
            oled_write_P(PSTR("Numpad\n"), false);
            break;
        case _NUMBER:
            oled_write_P(PSTR("Numbers\n"), false);
            break;
        case _SYMBOL:
            oled_write_P(PSTR("Symbol\n"), false);
            break;
        case _ADJUST:
            oled_write_P(PSTR("Adjust\n"), false);
            break;
        default:
            oled_write_P(PSTR("Undefined\n"), false);
    }

    // Host Keyboard Encoder Status
    oled_write_P(PSTR("Encoder: "), false);
    switch (enc_mode) {
      case BROWSER:
        oled_write_P(PSTR("Browser\n"), false);
        break;
      case VIMSCR:
        oled_write_P(PSTR("Vim Scroll\n"), false);
        break;
      case MEDIA:
        oled_write_P(PSTR("Media\n"), false);
        break;
      case MSWORD:
        oled_write_P(PSTR("MS Word\n"), false);
        break;
      default:
        oled_write_P(PSTR("Undefined\n"), false);
    }

    oled_write_P(PSTR("Combos: "), false);
    if (is_combo_enabled()) {
      oled_write_P(PSTR("on\n"), false);
    } else {
      oled_write_P(PSTR("off\n"), false);
    }

    // Host Keyboard LED Status
    uint8_t led_usb_state = host_keyboard_leds();
    oled_write_P(IS_LED_ON(led_usb_state, USB_LED_NUM_LOCK) ? PSTR("NUMLCK ") : PSTR("       "), false);
    oled_write_P(IS_LED_ON(led_usb_state, USB_LED_CAPS_LOCK) ? PSTR("CAPLCK ") : PSTR("       "), false);
    oled_write_P(IS_LED_ON(led_usb_state, USB_LED_SCROLL_LOCK) ? PSTR("SCRLCK ") : PSTR("       "), false);
}

void oled_task_user(void) {
    if (is_keyboard_master()) {
        render_status(); // Renders the current keyboard state (layer, lock, caps, scroll, etc)
    } else {
        render_puppy_logo();
        oled_scroll_right();
    }
}
